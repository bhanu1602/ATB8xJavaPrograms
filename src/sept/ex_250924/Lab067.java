package sept.ex_250924;

public class Lab067 {

    public static void main(String[] args) {
        int i = 10; // Initialize i to 10

        // For loop with initialization done before the loop; will run while i is greater than 0
        for (; i > 0; i--) {
            System.out.println(i); // Print the current value of i
        }
    }
}

