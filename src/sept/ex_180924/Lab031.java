package sept.ex_180924;

public class Lab031 {
    public static void main(String[] args) {

        // Assignment operator (=) and equality operator (==)

        int a = 10; // Assigning the value 10 to the variable 'a'
        boolean b = (10 == 11); // Comparison: checking if 10 is equal to 11 (returns false)

        System.out.println(a); // Output: 10 (value of a)
        System.out.println(b); // Output: false (since 10 is not equal to 11)
    }
}
