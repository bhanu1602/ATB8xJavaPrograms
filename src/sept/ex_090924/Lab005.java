package sept.ex_090924;

public class Lab005 {

    public static void main(String[] args) {

        // Keywords and Identifiers in Java

        /*
         * Keywords: These are predefined words in Java that have special meanings and cannot
         * be used as identifiers (class names, method names, variable names).
         * Java keywords are always in lowercase and reserved.
         *
         * Categories of Keywords:
         * 1. Data Types (8):
         *    - byte, short, int, long, float, double, char, boolean
         *
         * 2. Class and Object (9):
         *    - class, interface, enum, extends, implements, new, instanceof, this, super
         *
         * 3. Access Modifiers (3):
         *    - public, private, protected
         *
         * 4. Package (2):
         *    - package, import
         *
         * 5. Control Statements (12):
         *    - if, else, switch, case, default, while, do, for, break, continue, return, instanceof
         *
         * 6. Exception Handling (6):
         *    - try, catch, finally, throw, throws, assert
         *
         * 7. Other Keywords:
         *    - void (used for methods that don't return any value)
         */

        /*
         * Identifiers: These are names used to identify elements in a program like class names,
         * method names, variable names, etc.
         * - Rules for Identifiers:
         *    1. Identifiers can be a combination of alphabets (A-Z, a-z), digits (0-9), $ (dollar sign),
         *       and _ (underscore).
         *    2. They cannot begin with a digit.
         *    3. Java keywords cannot be used as identifiers.
         *
         * Example of valid identifiers:
         * - myVariable, class_name, $currency, _counter
         */

        int a = 10; // a is an identifier

        System.out.println(a);
    }
}

